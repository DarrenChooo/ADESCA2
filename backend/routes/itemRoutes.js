const express = require('express')

const router = express.Router()

const controller = require("../controller/itemController")

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/
/// ///////////////////////////////////////////////////
router.post("/", controller.createItems);

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/
/// ///////////////////////////////////////////////////
router.get("/", controller.getAllItems)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/:id
/// ///////////////////////////////////////////////////
router.get("/:id", controller.getItems)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/search/:itemname
/// ///////////////////////////////////////////////////
router.get("/search/:itemname", controller.getItemsByName)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/store/:floorid
/// ///////////////////////////////////////////////////
router.get("/store/:floorid", controller.getStoreItems)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/:id
/// ///////////////////////////////////////////////////
router.put("/:id", controller.updateItems)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/:id
/// ///////////////////////////////////////////////////
router.delete("/:id", controller.deleteItems)

/// ///////////////////////////////////////////////////
// Backend URL  -> http://localhost:3000/api/items/:id
/// ///////////////////////////////////////////////////
router.get("/stats/equippeditems", controller.retrieveEquippedItemData)

module.exports = router