const express = require('express')

const router = express.Router()

const controller = require("../controller/floorsController")

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/
/// ///////////////////////////////////////////////////
router.post("/", controller.insertFloor)

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/allFloors
/// ///////////////////////////////////////////////////
router.get("/allFloors", controller.getAllFloors)

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/<floorId>
/// ///////////////////////////////////////////////////
router.get("/:floorId", controller.getFloor)

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/name/<floorName>
/// ///////////////////////////////////////////////////
router.get("/name/:floorName", controller.getFloorByName)

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/<floorid>
/// ///////////////////////////////////////////////////
router.put("/:floorId", controller.updateFloor)

/// ///////////////////////////////////////////////////
// To test, postman -> http://localhost:3000/floor/<floorid> 
/// ///////////////////////////////////////////////////
router.delete("/:floorId", controller.deleteFloor)

module.exports = router